print( int(a) + int(b) )

# explicit typecasting
string_num = "15"
num = 5
print("The sum of string and a num is ", int(string_num) + num)

# implicit typecasting - here the python interpreter converts the datatype to float
c = 1.9
d = 8
print(c+d)