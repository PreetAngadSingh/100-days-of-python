# for loop
print("While loop")
for i in range(3):
    print(i)

# same code with while loop
print("While loop")
i=0
while(i<3):
    print(i)
    i=i+1

print("Done with the loops")

# while loop with else
count=5
while(count>0):
    print(count)
    count = count-1
else:
    print("Done with while, inside else")